import math

resultado = math.floor(3.766)
resultado2 = math.ceil(3.766)
print(f"El resultado de math.floor(3.766) es: {resultado}")
print(f"El resultado de math.ceil(3.766) es: {resultado2}")

# log is the logarithm function, which gives the logarithm of a number to a specified base.
# If the base is not provided, it defaults to e (Euler's number).
resultado3 = math.log(100, 10)
print(f"El resultado de math.log(100, 10) es: {resultado3}")

resultado4 = math.log(100)
print(f"El resultado de math.log(100) es: {resultado4}")    

# sqrt is the square root function, which returns the square root of a number.
resultado5 = math.sqrt(16)
print(f"El resultado de math.sqrt(16) es: {resultado5}")

# pow is the power function, which raises a number to the power of another number.
resultado6 = math.pow(2, 3)
print(f"El resultado de math.pow(2, 3) es: {resultado6}")

# factorial is the factorial function, which returns the factorial of a number.
# This is the product of all positive integers up to that number.
# For example, 5! (5 factorial) is 5 * 4 * 3 * 2 * 1 = 120.
resultado7 = math.factorial(5)
print(f"El resultado de math.factorial(5) es: {resultado7}")

